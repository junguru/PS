#include <bits/stdc++.h>
using namespace std;

int main(void) {
    ios_base::sync_with_stdio(0);
    cin.tie(0);

    int n, m;
    int board[505][505];

    cin >> n >> m;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            cin >> board[i][j];
        }
    }

    int ans = 0;
    int maxi, maxj, maxt;
    string tetromino[][4] = {
        {"1111", "0000", "0000", "0000"},
        {"1000", "1000", "1000", "1000"},
        {"1100", "1100", "0000", "0000"},
        {"1000", "1000", "1100", "0000"},
        {"1110", "1000", "0000", "0000"},
        {"1100", "0100", "0100", "0000"},
        {"0010", "1110", "0000", "0000"},
        {"0100", "0100", "1100", "0000"},
        {"1000", "1110", "0000", "0000"},
        {"1100", "1000", "1000", "0000"},
        {"1110", "0010", "0000", "0000"},
        {"1000", "1100", "0100", "0000"},
        {"0110", "1100", "0000", "0000"},
        {"0100", "1100", "1000", "0000"},
        {"1100", "0110", "0000", "0000"},
        {"1110", "0100", "0000", "0000"},
        {"0100", "1100", "0100", "0000"},
        {"0100", "1110", "0000", "0000"},
        {"1000", "1100", "1000", "0000"}
    };

    for (int t = 0; t < 19; t++) {
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < m; j++) {
                int cur = 0;
                bool oob = false;
                for (int k = 0; k < 4; k++) {
                    if (oob) break;
                    for (int l = 0; l < 4; l++) {
                        if (oob) break;
                        if (tetromino[t][k][l] == '1') {
                            if (i + k >= n || j + l >= m) {
                                oob = true;
                                break;
                            }
                            else {
                                cur += board[i+k][j+l];
                            }
                        }
                    }
                }
                if (!oob && cur > ans) {
                    ans = cur;
                }
            }
        }
    }

    cout << ans;
    return 0;
}